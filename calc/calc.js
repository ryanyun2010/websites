var num1=prompt("num 1");
var num2=prompt("num 2");
var type=prompt("type(+ - * /)");
if(type=="+"){
	alert(parseFloat(num1)+parseFloat(num2));
}if(type=="-"){
	alert(parseFloat(num1)-parseFloat(num2));
}if(type=="*"){
	alert(parseFloat(num1)*parseFloat(num2));
}if(type=="/"){
	alert(parseFloat(num1)/parseFloat(num2));
}
